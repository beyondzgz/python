# if
score = 75
if score >= 60:
    print('passed')

# if-else
score = 55
if score >= 60:
    print('passed')
else:
    print('failed')

# if-elif-else
score = 85
if score >= 90:
    print('excellent')
elif score >= 80:
    print('good')
elif score >= 60:
    print('passed')
else:
    print('failed')
